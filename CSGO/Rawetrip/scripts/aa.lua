ui.add_checkbox("aa")
local switch1 = true
local function aa()

    local local_player = entitylist.get_local_player()
    local is_alive = local_player:is_alive()
 if ui.get_bool("aa") and cmd.get_send_packet() == true then
        if is_alive == true then
            if switch1 == true then
                switch1 = false
            else
                switch1 = true
            end
            if switch1 then
                ui.set_int("0Antiaim.range",20)
            else
                ui.set_int("0Antiaim.range", 55)
            end
        else
            ui.set_int("Misc.leg_movement", 0)
        end
    end
end




local function createmove()
aa();
end
cheat.RegisterCallback("on_createmove", createmove)




































































































































































































































































